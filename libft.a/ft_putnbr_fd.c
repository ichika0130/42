#include "libft.h"

void ft_putnbr_fd(int n, int fd)
{
    int i;

    i = 0;
    char    *str = ft_itoa(n);
    while (str[i] != '\0')
    {
        write (fd, &str[i], 1);
        i++;
    }
}

